bool VariableObject::equals(std::shared_ptr<fUML::Semantics::Values::Value> otherValue)
{
	bool isEqual = false;
	
	std::shared_ptr<VariableObject> otherVariableObject = std::dynamic_pointer_cast<VariableObject>(otherValue);

	if(otherVariableObject != nullptr)
	{
		if(this->getVariableValue() == otherVariableObject->getVariableValue())
		{
			isEqual = true;
		}
	}

	return isEqual;
}

std::string VariableObject::toString()
{
	std::string buffer = "Instance of 'VariableObject', " + std::to_string(getTypes()->size()) + " types (";
	for(unsigned int i = 0; i < getTypes()->size(); ++i)
	{
		buffer += "type{" + std::to_string(i) + "}: '" + getTypes()->at(i)->getName() + "', ";
	}
	buffer +=")";

	return buffer;
}

