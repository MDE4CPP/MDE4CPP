bool OperationObject::equals(std::shared_ptr<fUML::Semantics::Values::Value> otherValue)
{
	bool isEqual = false;
	
	std::shared_ptr<OperationObject> otherOperationObject = std::dynamic_pointer_cast<OperationObject>(otherValue);

	if(otherOperationObject != nullptr)
	{
		if(this->getOperationValue() == otherOperationObject->getOperationValue())
		{
			isEqual = true;
		}
	}

	return isEqual;
}

std::string OperationObject::toString()
{
	std::string buffer = "Instance of 'OperationObject', " + std::to_string(getTypes()->size()) + " types (";
	for(unsigned int i = 0; i < getTypes()->size(); ++i)
	{
		buffer += "type{" + std::to_string(i) + "}: '" + getTypes()->at(i)->getName() + "', ";
	}
	buffer +=")";

	return buffer;
}

