void DeferredEventOccurrenceImpl::resolveReferences(const int featureID, std::vector<std::shared_ptr<ecore::EObject>> references)
{
	switch(featureID)
	{
		case PSSM::Semantics::StateMachines::StateMachinesPackage::DEFERREDEVENTOCCURRENCE_ATTRIBUTE_CONSTRAININGSTATEACTIVATION:
		{
			if (references.size() == 1)
			{
				// Cast object to correct type
				std::shared_ptr<PSSM::Semantics::StateMachines::StateActivation> _constrainingStateActivation = std::dynamic_pointer_cast<PSSM::Semantics::StateMachines::StateActivation>( references.front() );
				setConstrainingStateActivation(_constrainingStateActivation);
			}
			
			return;
		}

		case PSSM::Semantics::StateMachines::StateMachinesPackage::DEFERREDEVENTOCCURRENCE_ATTRIBUTE_DEFERREDEVENTOCCURRENCE:
		{
			if (references.size() == 1)
			{
				// Cast object to correct type
				std::shared_ptr<fUML::Semantics::CommonBehavior::EventOccurrence> _deferredEventOccurrence = std::dynamic_pointer_cast<fUML::Semantics::CommonBehavior::EventOccurrence>( references.front() );
				setDeferredEventOccurrence(_deferredEventOccurrence);
			}
			
			return;
		}
	}
	fUML::Semantics::CommonBehavior::EventOccurrenceImpl::resolveReferences(featureID, references);
}

