bool ExpressionObject::equals(std::shared_ptr<fUML::Semantics::Values::Value> otherValue)
{
	bool isEqual = false;
	
	std::shared_ptr<ExpressionObject> otherExpressionObject = std::dynamic_pointer_cast<ExpressionObject>(otherValue);

	if(otherExpressionObject != nullptr)
	{
		if(this->getExpressionValue() == otherExpressionObject->getExpressionValue())
		{
			isEqual = true;
		}
	}

	return isEqual;
}

