bool TransitionObject::equals(std::shared_ptr<fUML::Semantics::Values::Value> otherValue)
{
	bool isEqual = false;
	
	std::shared_ptr<TransitionObject> otherTransitionObject = std::dynamic_pointer_cast<TransitionObject>(otherValue);

	if(otherTransitionObject != nullptr)
	{
		if(this->getTransitionValue() == otherTransitionObject->getTransitionValue())
		{
			isEqual = true;
		}
	}

	return isEqual;
}

