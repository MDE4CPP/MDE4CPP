{
	switch(featureID)
	{
		case fUML::Semantics::StructuredClassifiers::StructuredClassifiersPackage::LINK_ATTRIBUTE_TYPE:
			return getType() != nullptr; //682
	}
	return ExtensionalValueImpl::internalEIsSet(featureID);
}

bool LinkImpl::eSet(int featureID, Any newValue)
{
	switch(featureID)
	{
		case fUML::Semantics::StructuredClassifiers::StructuredClassifiersPackage::LINK_ATTRIBUTE_TYPE:
		{
			// CAST Any to uml::Association
			std::shared_ptr<ecore::EObject> _temp = newValue->get<std::shared_ptr<ecore::EObject>>();
			std::shared_ptr<uml::Association> _type = std::dynamic_pointer_cast<uml::Association>(_temp);
			setType(_type); //682
			return true;
		}
	}

	return ExtensionalValueImpl::eSet(featureID, newValue);
}

//*********************************
// EOperation Invoke
//*********************************
Any LinkImpl::eInvoke(int operationID, std::shared_ptr<std::list<Any>> arguments)
{
	Any result;
 
  	switch(operationID)
	{
		// fUML::Semantics::StructuredClassifiers::Link::_copy() : fUML::Semantics::Values::Value: 3125386239
		case StructuredClassifiersPackage::LINK_OPERATION__COPY:
		{
			result = eAnyObject(this->_copy(), fUML::Semantics::Values::ValuesPackage::VALUE_CLASS);
			break;
		}
		// fUML::Semantics::StructuredClassifiers::Link::addTo(fUML::Semantics::Loci::Locus): 3672267639
		case StructuredClassifiersPackage::LINK_OPERATION_ADDTO_LOCUS:
		{
			//Retrieve input parameter 'locus'
			//parameter 0
			std::shared_ptr<fUML::Semantics::Loci::Locus> incoming_param_locus;
			std::list<Any>::const_iterator incoming_param_locus_arguments_citer = std::next(arguments->begin(), 0);
			incoming_param_locus = (*incoming_param_locus_arguments_citer)->get<std::shared_ptr<fUML::Semantics::Loci::Locus> >();
			this->addTo(incoming_param_locus);
			break;
		}
		// fUML::Semantics::StructuredClassifiers::Link::getOtherFeatureValues(fUML::Semantics::StructuredClassifiers::ExtensionalValue[*], uml::Property) : fUML::Semantics::SimpleClassifiers::FeatureValue[*]: 1256957395
		case StructuredClassifiersPackage::LINK_OPERATION_GETOTHERFEATUREVALUES_EXTENSIONALVALUE_PROPERTY:
		{
			//Retrieve input parameter 'extent'
			//parameter 0
			std::shared_ptr<Bag<fUML::Semantics::StructuredClassifiers::ExtensionalValue>> incoming_param_extent;
			std::list<Any>::const_iterator incoming_param_extent_arguments_citer = std::next(arguments->begin(), 0);
			incoming_param_extent = (*incoming_param_extent_arguments_citer)->get<std::shared_ptr<Bag<fUML::Semantics::StructuredClassifiers::ExtensionalValue>> >();
			//Retrieve input parameter 'end'
			//parameter 1
			std::shared_ptr<uml::Property> incoming_param_end;
			std::list<Any>::const_iterator incoming_param_end_arguments_citer = std::next(arguments->begin(), 1);
			incoming_param_end = (*incoming_param_end_arguments_citer)->get<std::shared_ptr<uml::Property> >();
			std::shared_ptr<Bag<fUML::Semantics::SimpleClassifiers::FeatureValue> > resultList = this->getOtherFeatureValues(incoming_param_extent,incoming_param_end);
			return eAnyBag(resultList,fUML::Semantics::SimpleClassifiers::SimpleClassifiersPackage::FEATUREVALUE_CLASS);
			break;
		}
		// fUML::Semantics::StructuredClassifiers::Link::getTypes() : uml::Classifier[*] {const}: 2319995210
		case StructuredClassifiersPackage::LINK_OPERATION_GETTYPES:
		{
			std::shared_ptr<Bag<uml::Classifier> > resultList = this->getTypes();
			return eAnyBag(resultList,uml::umlPackage::CLASSIFIER_CLASS);
			break;
		}
		// fUML::Semantics::StructuredClassifiers::Link::isMatchingLink(fUML::Semantics::StructuredClassifiers::ExtensionalValue, uml::Property) : bool: 995981000
		case StructuredClassifiersPackage::LINK_OPERATION_ISMATCHINGLINK_EXTENSIONALVALUE_PROPERTY:
		{
			//Retrieve input parameter 'link'
			//parameter 0
			std::shared_ptr<fUML::Semantics::StructuredClassifiers::ExtensionalValue> incoming_param_link;
			std::list<Any>::const_iterator incoming_param_link_arguments_citer = std::next(arguments->begin(), 0);
			incoming_param_link = (*incoming_param_link_arguments_citer)->get<std::shared_ptr<fUML::Semantics::StructuredClassifiers::ExtensionalValue> >();
			//Retrieve input parameter 'end'
			//parameter 1
			std::shared_ptr<uml::Property> incoming_param_end;
			std::list<Any>::const_iterator incoming_param_end_arguments_citer = std::next(arguments->begin(), 1);
			incoming_param_end = (*incoming_param_end_arguments_citer)->get<std::shared_ptr<uml::Property> >();
			result = eAny(this->isMatchingLink(incoming_param_link,incoming_param_end),0,false);
			break;
		}
		// fUML::Semantics::StructuredClassifiers::Link::new_() : fUML::Semantics::Values::Value: 3155204086
		case StructuredClassifiersPackage::LINK_OPERATION_NEW_:
		{
			result = eAnyObject(this->new_(), fUML::Semantics::Values::ValuesPackage::VALUE_CLASS);
			break;
		}

		default:
		{
			// call superTypes
			result = ExtensionalValueImpl::eInvoke(operationID, arguments);
			if (result && !result->isEmpty())
				break;
			break;
		}
  	}

	return result;
}

