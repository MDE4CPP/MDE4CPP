void DurationObservationObject::removeValue(std::shared_ptr<uml::StructuralFeature> feature, std::shared_ptr<fUML::Semantics::Values::Value> value)
{
	if (feature->getMetaElementID() != uml::umlPackage::PROPERTY_CLASS && feature->getMetaElementID() != uml::umlPackage::PORT_CLASS && feature->getMetaElementID() != uml::umlPackage::EXTENSIONEND_CLASS)
	{
		std::cerr << __PRETTY_FUNCTION__ << ": feature is null or not kind of uml::Property" << std::endl;
		throw "feature is null or not kind of uml::Property";
	}

	if (m_DurationObservationValue == nullptr)
	{
		std::cerr << __PRETTY_FUNCTION__ << ": no value stored inside DurationObservationObject (property: " << feature->getName() << ")" << std::endl;
		throw "NullPointerException";	
	}

	if (feature == UML::UMLPackage::eInstance()->get_UML_DurationObservation_event())
	{
		if (value == nullptr) // clear mode
		{
			m_DurationObservationValue->getEvent()->clear();
		}
		else
		{
			/* Should use PSCS::CS_Reference but dynamic_pointer_cast fails --> using fUML::Reference instead
			std::shared_ptr<PSCS::Semantics::StructuredClassifiers::CS_Reference> reference = std::dynamic_pointer_cast<PSCS::Semantics::StructuredClassifiers::CS_Reference>(value); */
			std::shared_ptr<fUML::Semantics::StructuredClassifiers::Reference> reference = std::dynamic_pointer_cast<fUML::Semantics::StructuredClassifiers::Reference>(value);
			std::shared_ptr<UML::NamedElementObject> inputValue = std::dynamic_pointer_cast<UML::NamedElementObject>(reference->getReferent());
			if (inputValue != nullptr)
			{
				m_DurationObservationValue->getEvent()->erase(inputValue->getNamedElementValue());
			}
		}
	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_DurationObservation_firstEvent())
	{
		if (value == nullptr) // clear mode
		{
			m_DurationObservationValue->isFirstEvent()->clear();
		}
		else
		{
			std::shared_ptr<fUML::Semantics::SimpleClassifiers::BooleanValue> inputValue = std::dynamic_pointer_cast<fUML::Semantics::SimpleClassifiers::BooleanValue>(value);
			if (inputValue != nullptr)
			{
				bool specificValue = inputValue->isValue();
				std::shared_ptr<Bag<bool>> valueList = m_DurationObservationValue->isFirstEvent();
				Bag<bool>::iterator iter = valueList->begin();
				Bag<bool>::iterator end = valueList->end();
				while (iter != end)
				{
					if (specificValue == *(*iter))
					{
						valueList->erase(iter);
						break;
					}
					iter++;
				}
			}
		}
	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_Element_ownedComment())
	{
		if (value == nullptr) // clear mode
		{
			m_DurationObservationValue->getOwnedComment()->clear();
		}
		else
		{
			/* Should use PSCS::CS_Reference but dynamic_pointer_cast fails --> using fUML::Reference instead
			std::shared_ptr<PSCS::Semantics::StructuredClassifiers::CS_Reference> reference = std::dynamic_pointer_cast<PSCS::Semantics::StructuredClassifiers::CS_Reference>(value); */
			std::shared_ptr<fUML::Semantics::StructuredClassifiers::Reference> reference = std::dynamic_pointer_cast<fUML::Semantics::StructuredClassifiers::Reference>(value);
			std::shared_ptr<UML::CommentObject> inputValue = std::dynamic_pointer_cast<UML::CommentObject>(reference->getReferent());
			if (inputValue != nullptr)
			{
				m_DurationObservationValue->getOwnedComment()->erase(inputValue->getCommentValue());
			}
		}
	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_NamedElement_name())
	{
				// no default value defined, clear not realized

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_NamedElement_nameExpression())
	{
				//m_DurationObservationValue->getNameExpression().reset();

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_NamedElement_visibility())
	{
				// no default value defined, clear not realized

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_PackageableElement_visibility())
	{
				m_DurationObservationValue->setVisibility(uml::VisibilityKind::PUBLIC /*defined default value*/);

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_ParameterableElement_owningTemplateParameter())
	{
				//m_DurationObservationValue->getOwningTemplateParameter().reset();

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_ParameterableElement_templateParameter())
	{
				//m_DurationObservationValue->getTemplateParameter().reset();

	}
}


