bool IncludeObject::equals(std::shared_ptr<fUML::Semantics::Values::Value> otherValue)
{
	bool isEqual = false;
	
	std::shared_ptr<IncludeObject> otherIncludeObject = std::dynamic_pointer_cast<IncludeObject>(otherValue);

	if(otherIncludeObject != nullptr)
	{
		if(this->getIncludeValue() == otherIncludeObject->getIncludeValue())
		{
			isEqual = true;
		}
	}

	return isEqual;
}

