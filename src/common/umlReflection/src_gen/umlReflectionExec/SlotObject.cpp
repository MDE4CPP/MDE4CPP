bool SlotObject::equals(std::shared_ptr<fUML::Semantics::Values::Value> otherValue)
{
	bool isEqual = false;
	
	std::shared_ptr<SlotObject> otherSlotObject = std::dynamic_pointer_cast<SlotObject>(otherValue);

	if(otherSlotObject != nullptr)
	{
		if(this->getSlotValue() == otherSlotObject->getSlotValue())
		{
			isEqual = true;
		}
	}

	return isEqual;
}

