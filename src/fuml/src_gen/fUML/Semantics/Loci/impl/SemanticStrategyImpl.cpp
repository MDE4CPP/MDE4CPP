//*********************************
// EOperation Invoke
//*********************************
std::shared_ptr<Any> SemanticStrategyImpl::eInvoke(int operationID, const std::shared_ptr<Bag<Any>>& arguments)
{
	std::shared_ptr<Any> result;
 
  	switch(operationID)
	{
		// fUML::Semantics::Loci::SemanticStrategy::getName() : std::string: 3310607623
		case LociPackage::SEMANTICSTRATEGY_OPERATION_GETNAME:
		{
			result = eAny(this->getName(), 0, false);
			break;
		}

		default:
		{
			// call superTypes
			result = ecore::EModelElementImpl::eInvoke(operationID, arguments);
			if (result && !result->isEmpty())
				break;
			break;
		}
  	}

	return result;
}

