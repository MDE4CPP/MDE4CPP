void ObjectFlowObject::removeValue(std::shared_ptr<uml::StructuralFeature> feature, std::shared_ptr<fUML::Semantics::Values::Value> value)
{
	if (feature->getMetaElementID() != uml::umlPackage::PROPERTY_CLASS && feature->getMetaElementID() != uml::umlPackage::PORT_CLASS && feature->getMetaElementID() != uml::umlPackage::EXTENSIONEND_CLASS)
	{
		std::cerr << __PRETTY_FUNCTION__ << ": feature is null or not kind of uml::Property" << std::endl;
		throw "feature is null or not kind of uml::Property";
	}

	if (m_ObjectFlowValue == nullptr)
	{
		std::cerr << __PRETTY_FUNCTION__ << ": no value stored inside ObjectFlowObject (property: " << feature->getName() << ")" << std::endl;
		throw "NullPointerException";	
	}

	if (feature == UML::UMLPackage::eInstance()->get_UML_ActivityEdge_activity())
	{
				m_ObjectFlowValue->getActivity().reset();

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_ActivityEdge_guard())
	{
				m_ObjectFlowValue->getGuard().reset();

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_ActivityEdge_inPartition())
	{
		if (value == nullptr) // clear mode
		{
			m_ObjectFlowValue->getInPartition()->clear();
		}
		else
		{
			/* Should use PSCS::CS_Reference but dynamic_pointer_cast fails --> using fUML::Reference instead
			std::shared_ptr<PSCS::Semantics::StructuredClassifiers::CS_Reference> reference = std::dynamic_pointer_cast<PSCS::Semantics::StructuredClassifiers::CS_Reference>(value); */
			std::shared_ptr<fUML::Semantics::StructuredClassifiers::Reference> reference = std::dynamic_pointer_cast<fUML::Semantics::StructuredClassifiers::Reference>(value);
			std::shared_ptr<UML::ActivityPartitionObject> inputValue = std::dynamic_pointer_cast<UML::ActivityPartitionObject>(reference->getReferent());
			if (inputValue != nullptr)
			{
				m_ObjectFlowValue->getInPartition()->erase(inputValue->getActivityPartitionValue());
			}
		}
	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_ActivityEdge_inStructuredNode())
	{
				m_ObjectFlowValue->getInStructuredNode().reset();

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_ActivityEdge_interrupts())
	{
				m_ObjectFlowValue->getInterrupts().reset();

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_ActivityEdge_redefinedEdge())
	{
		if (value == nullptr) // clear mode
		{
			m_ObjectFlowValue->getRedefinedEdge()->clear();
		}
		else
		{
			/* Should use PSCS::CS_Reference but dynamic_pointer_cast fails --> using fUML::Reference instead
			std::shared_ptr<PSCS::Semantics::StructuredClassifiers::CS_Reference> reference = std::dynamic_pointer_cast<PSCS::Semantics::StructuredClassifiers::CS_Reference>(value); */
			std::shared_ptr<fUML::Semantics::StructuredClassifiers::Reference> reference = std::dynamic_pointer_cast<fUML::Semantics::StructuredClassifiers::Reference>(value);
			std::shared_ptr<UML::ActivityEdgeObject> inputValue = std::dynamic_pointer_cast<UML::ActivityEdgeObject>(reference->getReferent());
			if (inputValue != nullptr)
			{
				m_ObjectFlowValue->getRedefinedEdge()->erase(inputValue->getActivityEdgeValue());
			}
		}
	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_ActivityEdge_source())
	{
				m_ObjectFlowValue->getSource().reset();

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_ActivityEdge_target())
	{
				m_ObjectFlowValue->getTarget().reset();

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_ActivityEdge_weight())
	{
				m_ObjectFlowValue->getWeight().reset();

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_Element_ownedComment())
	{
		if (value == nullptr) // clear mode
		{
			m_ObjectFlowValue->getOwnedComment()->clear();
		}
		else
		{
			/* Should use PSCS::CS_Reference but dynamic_pointer_cast fails --> using fUML::Reference instead
			std::shared_ptr<PSCS::Semantics::StructuredClassifiers::CS_Reference> reference = std::dynamic_pointer_cast<PSCS::Semantics::StructuredClassifiers::CS_Reference>(value); */
			std::shared_ptr<fUML::Semantics::StructuredClassifiers::Reference> reference = std::dynamic_pointer_cast<fUML::Semantics::StructuredClassifiers::Reference>(value);
			std::shared_ptr<UML::CommentObject> inputValue = std::dynamic_pointer_cast<UML::CommentObject>(reference->getReferent());
			if (inputValue != nullptr)
			{
				m_ObjectFlowValue->getOwnedComment()->erase(inputValue->getCommentValue());
			}
		}
	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_NamedElement_name())
	{
				// no default value defined, clear not realized

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_NamedElement_nameExpression())
	{
				m_ObjectFlowValue->getNameExpression().reset();

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_NamedElement_visibility())
	{
				// no default value defined, clear not realized

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_ObjectFlow_isMulticast())
	{
				m_ObjectFlowValue->setIsMulticast(false /*defined default value*/);

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_ObjectFlow_isMultireceive())
	{
				m_ObjectFlowValue->setIsMultireceive(false /*defined default value*/);

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_ObjectFlow_selection())
	{
				m_ObjectFlowValue->getSelection().reset();

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_ObjectFlow_transformation())
	{
				m_ObjectFlowValue->getTransformation().reset();

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_RedefinableElement_isLeaf())
	{
				m_ObjectFlowValue->setIsLeaf(false /*defined default value*/);

	}
}


