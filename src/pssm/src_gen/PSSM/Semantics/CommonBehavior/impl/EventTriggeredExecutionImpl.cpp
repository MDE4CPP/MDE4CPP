std::shared_ptr<Any> EventTriggeredExecutionImpl::eInvoke(int operationID, const std::shared_ptr<Bag<Any>>& arguments)
{
	std::shared_ptr<Any> result;
 
  	switch(operationID)
	{
		// PSSM::Semantics::CommonBehavior::EventTriggeredExecution::copy() : Any: 285431960
		case CommonBehaviorPackage::EVENTTRIGGEREDEXECUTION_OPERATION_COPY:
		{
			result = eAny(this->copy(), 0, false);
			break;
		}

		default:
		{
			// call superTypes
			result = fUML::Semantics::CommonBehavior::ExecutionImpl::eInvoke(operationID, arguments);
			if (result && !result->isEmpty())
				break;
			break;
		}
  	}

	return result;
}

