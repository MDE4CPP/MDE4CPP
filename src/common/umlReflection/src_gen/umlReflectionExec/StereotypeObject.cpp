bool StereotypeObject::equals(std::shared_ptr<fUML::Semantics::Values::Value> otherValue)
{
	bool isEqual = false;
	
	std::shared_ptr<StereotypeObject> otherStereotypeObject = std::dynamic_pointer_cast<StereotypeObject>(otherValue);

	if(otherStereotypeObject != nullptr)
	{
		if(this->getStereotypeValue() == otherStereotypeObject->getStereotypeValue())
		{
			isEqual = true;
		}
	}

	return isEqual;
}

std::string StereotypeObject::toString()
{
	std::string buffer = "Instance of 'StereotypeObject', " + std::to_string(getTypes()->size()) + " types (";
	for(unsigned int i = 0; i < getTypes()->size(); ++i)
	{
		buffer += "type{" + std::to_string(i) + "}: '" + getTypes()->at(i)->getName() + "', ";
	}
	buffer +=")";

	return buffer;
}

