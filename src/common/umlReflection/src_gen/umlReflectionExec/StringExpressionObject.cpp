bool StringExpressionObject::equals(std::shared_ptr<fUML::Semantics::Values::Value> otherValue)
{
	bool isEqual = false;
	
	std::shared_ptr<StringExpressionObject> otherStringExpressionObject = std::dynamic_pointer_cast<StringExpressionObject>(otherValue);

	if(otherStringExpressionObject != nullptr)
	{
		if(this->getStringExpressionValue() == otherStringExpressionObject->getStringExpressionValue())
		{
			isEqual = true;
		}
	}

	return isEqual;
}

