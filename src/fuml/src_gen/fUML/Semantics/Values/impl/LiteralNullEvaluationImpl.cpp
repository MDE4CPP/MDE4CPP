//*********************************
// EOperation Invoke
//*********************************
Any LiteralNullEvaluationImpl::eInvoke(int operationID, std::shared_ptr<std::list<Any>> arguments)
{
	Any result;
 
  	switch(operationID)
	{
		// fUML::Semantics::Values::LiteralNullEvaluation::evaluate() : Any: 3505594073
		case ValuesPackage::LITERALNULLEVALUATION_OPERATION_EVALUATE:
		{
			result = this->evaluate();
			break;
		}

		default:
		{
			// call superTypes
			result = LiteralEvaluationImpl::eInvoke(operationID, arguments);
			if (result && !result->isEmpty())
				break;
			break;
		}
  	}

	return result;
}

