bool RegionObject::equals(std::shared_ptr<fUML::Semantics::Values::Value> otherValue)
{
	bool isEqual = false;
	
	std::shared_ptr<RegionObject> otherRegionObject = std::dynamic_pointer_cast<RegionObject>(otherValue);

	if(otherRegionObject != nullptr)
	{
		if(this->getRegionValue() == otherRegionObject->getRegionValue())
		{
			isEqual = true;
		}
	}

	return isEqual;
}

std::string RegionObject::toString()
{
	std::string buffer = "Instance of 'RegionObject', " + std::to_string(getTypes()->size()) + " types (";
	for(unsigned int i = 0; i < getTypes()->size(); ++i)
	{
		buffer += "type{" + std::to_string(i) + "}: '" + getTypes()->at(i)->getName() + "', ";
	}
	buffer +=")";

	return buffer;
}

