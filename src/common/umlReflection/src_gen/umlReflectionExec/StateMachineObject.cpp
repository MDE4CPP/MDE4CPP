bool StateMachineObject::equals(std::shared_ptr<fUML::Semantics::Values::Value> otherValue)
{
	bool isEqual = false;
	
	std::shared_ptr<StateMachineObject> otherStateMachineObject = std::dynamic_pointer_cast<StateMachineObject>(otherValue);

	if(otherStateMachineObject != nullptr)
	{
		if(this->getStateMachineValue() == otherStateMachineObject->getStateMachineValue())
		{
			isEqual = true;
		}
	}

	return isEqual;
}

std::string StateMachineObject::toString()
{
	std::string buffer = "Instance of 'StateMachineObject', " + std::to_string(getTypes()->size()) + " types (";
	for(unsigned int i = 0; i < getTypes()->size(); ++i)
	{
		buffer += "type{" + std::to_string(i) + "}: '" + getTypes()->at(i)->getName() + "', ";
	}
	buffer +=")";

	return buffer;
}

