void LinkEndDestructionDataObject::removeValue(std::shared_ptr<uml::StructuralFeature> feature, std::shared_ptr<fUML::Semantics::Values::Value> value)
{
	if (feature->getMetaElementID() != uml::UmlPackage::PROPERTY_CLASS && feature->getMetaElementID() != uml::UmlPackage::PORT_CLASS && feature->getMetaElementID() != uml::UmlPackage::EXTENSIONEND_CLASS)
	{
		std::cerr << __PRETTY_FUNCTION__ << ": feature is null or not kind of uml::Property" << std::endl;
		throw "feature is null or not kind of uml::Property";
	}

	if (m_LinkEndDestructionDataValue == nullptr)
	{
		std::cerr << __PRETTY_FUNCTION__ << ": no value stored inside LinkEndDestructionDataObject (property: " << feature->getName() << ")" << std::endl;
		throw "NullPointerException";	
	}

	if (feature == UML::UMLPackage::eInstance()->get_UML_Element_ownedComment())
	{
		if (value == nullptr) // clear mode
		{
			m_LinkEndDestructionDataValue->getOwnedComment()->clear();
		}
		else
		{
			/* Should use PSCS::CS_Reference but dynamic_pointer_cast fails --> using fUML::Reference instead
			std::shared_ptr<PSCS::Semantics::StructuredClassifiers::CS_Reference> reference = std::dynamic_pointer_cast<PSCS::Semantics::StructuredClassifiers::CS_Reference>(value); */
			std::shared_ptr<fUML::Semantics::StructuredClassifiers::Reference> reference = std::dynamic_pointer_cast<fUML::Semantics::StructuredClassifiers::Reference>(value);
			std::shared_ptr<UML::CommentObject> inputValue = std::dynamic_pointer_cast<UML::CommentObject>(reference->getReferent());
			if (inputValue != nullptr)
			{
				m_LinkEndDestructionDataValue->getOwnedComment()->erase(inputValue->getCommentValue());
			}
		}
	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_LinkEndData_end())
	{
				m_LinkEndDestructionDataValue->getEnd().reset();

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_LinkEndData_qualifier())
	{
		if (value == nullptr) // clear mode
		{
			m_LinkEndDestructionDataValue->getQualifier()->clear();
		}
		else
		{
			/* Should use PSCS::CS_Reference but dynamic_pointer_cast fails --> using fUML::Reference instead
			std::shared_ptr<PSCS::Semantics::StructuredClassifiers::CS_Reference> reference = std::dynamic_pointer_cast<PSCS::Semantics::StructuredClassifiers::CS_Reference>(value); */
			std::shared_ptr<fUML::Semantics::StructuredClassifiers::Reference> reference = std::dynamic_pointer_cast<fUML::Semantics::StructuredClassifiers::Reference>(value);
			std::shared_ptr<UML::QualifierValueObject> inputValue = std::dynamic_pointer_cast<UML::QualifierValueObject>(reference->getReferent());
			if (inputValue != nullptr)
			{
				m_LinkEndDestructionDataValue->getQualifier()->erase(inputValue->getQualifierValueValue());
			}
		}
	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_LinkEndData_value())
	{
				m_LinkEndDestructionDataValue->getValue().reset();

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_LinkEndDestructionData_destroyAt())
	{
				m_LinkEndDestructionDataValue->getDestroyAt().reset();

	}
	if (feature == UML::UMLPackage::eInstance()->get_UML_LinkEndDestructionData_isDestroyDuplicates())
	{
				m_LinkEndDestructionDataValue->setIsDestroyDuplicates(false /*defined default value*/);

	}
}


